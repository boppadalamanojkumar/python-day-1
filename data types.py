# EXPLANATION:-

#int        (Integer (whole numbers))             ex:- 10, -5, 100
#float      (Decimal numbers)                     ex:- 3.14, 5.0
#str        (Text/String)                         ex:- "Hello"
#bool       (True or False)	                  ex:- True, False
#complex    (Complex numbers)                     ex:- 2+3j
#list       (Ordered, changeable collection)      ex:- [1, 2, 3]
#tuple	    (Ordered, unchangeable collection	  ex:- (1, 2, 3)
#set	    (Unordered collection of unique items)ex:- {1, 2, 3}
#dict	    (Key-value pairs)                     ex:- {"name":"Manoj"}
#range	    (Sequence of numbers)	          ex:- range(5)
